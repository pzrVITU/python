#Operadores aritmetricos

adicao = 10 + 10 # Pode ser ente números inteiros, ou numeros com ponto flutuante (int + int ; int + float ; float + int ; float + float)
print ("Adição", adicao)

subtracao = 10- 5
print ("Subtração: ", subtracao) # Pode ser ente números inteiros, ou numeros com ponto flutuante (int + int ; int + float ; float + int ; float + float)

multiplicacao = 10 * 10 
print ("Multiplicação", multiplicacao) # Pode ser ente números inteiros, ou numeros com ponto flutuante (int + int ; int + float ; float + int ; float + float)

divisao = 10/2.2 # Sempre retorna um número de ponto flutuante, independente dos valores que for preenchidos
print ('Divisão', divisao) # Pode ser ente números inteiros, ou numeros com ponto flutuante (int + int ; int + float ; float + int ; float + float)

divisao_inteira = 10 // 2.2 # Pode até retornar o valor em ponto flutuante, porém ela trunca as casas decimais, retornando somente o valor inteiro
print ('Divisão inteira', divisao_inteira) # Pode ser ente números inteiros, ou numeros com ponto flutuante (int + int ; int + float ; float + int ; float + float)

exponenciacao = 2 ** 10
print ('Exponenciação', exponenciacao)

modulo = 55 % 2 # Resto da divisão
print ('Módulo', modulo)
