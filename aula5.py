# Tipo de dado bool (boolean)
# Ao questionar algo em um programa, so existe duas respostas possíveis, sim (True) ou não (False).
# Existem varios operadores para "questionar", dentre eles o ==, que é um operador lógico que questiona se um valor é igual ao outro

print(10 == 10) # Sim => True (Verdadeiro)
print(10 < 9) # Não, 9 não é maior que 10 -> False (Falso)
print(type(10 < 9)) # Tentativa de utilizar o type para retorno do tipo de dado. (tipo retornado com sucesso)
