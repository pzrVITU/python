# Tipos int e float
# int -> número inteiro (números sem casas decimais)

print(11) #int
print(-11) #int
print(0) #int

# float -> número com ponto flutuante
# O tipo float representa qulaquer número positivo ou negativo com ponto flutuante.
# Float sem sinal é considerado positivo

print(1.1, 10.11)
print(0.0, -1.5)


# A função type mostra o tipo que o Python inferiu ao valor.
print (type('Henrique')) #Função type é utilizada para diferentes funções no Python, nesse momento o foco é para ela retornar o tipo de dados que a linguagem definiu para aquele valor, conforme os exemplos.
print (type(0))
print (type(1.5))
