# Estrutura condicionais

# if/ elif .... / else
# se/ se nao se / se não

#Iremos alterar o fluxo do nosso programa

entrada = input('Voce quer "entrar" ou "sair"? ')

if entrada == 'entrar':
    print(f'Voce entrou no sistema ')
elif entrada == 'sair':
    print(f'Voce saiu do sistema ')
else:
    print(f'Digite algo válido ')